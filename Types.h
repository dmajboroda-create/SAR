#pragma once
#include <array>

/**
 * @file Types.h
 * @brief Базові типи даних для системи автоматичного регулювання авіаційного двигуна.
 *
 * Містить визначення вектора стану, вказівника на функцію правої частини
 * системи диференціальних рівнянь та константу порядку системи.
 *
 * @author Мажборода Д. М.
 * @date 2025
 */

/**
 * @brief Порядок системи диференціальних рівнянь.
 *
 * Диференціальне рівняння авіаційного двигуна має 4-й порядок,
 * тому система зводиться до 4 рівнянь 1-го порядку.
 */
constexpr size_t SYSTEM_ORDER = 4;

/**
 * @brief Вектор стану системи у поточний момент часу.
 *
 * Вектор містить: {x, x', x'', x'''}, де:
 * - x      — кількість обертів валу турбіни (об/с)
 * - x'     — перша похідна (швидкість зміни обертів)
 * - x''    — друга похідна (прискорення)
 * - x'''   — третя похідна
 */
using State = std::array<double, SYSTEM_ORDER>;

/**
 * @brief Тип вказівника на функцію правої частини системи ДР.
 *
 * Функція обчислює вектор похідних dy/dt = f(t, y).
 *
 * @param t Поточний час (секунди)
 * @param y Поточний вектор стану
 * @return Вектор похідних у момент часу t
 */
using DerivativeFunc = State (*)(double t, const State& y);